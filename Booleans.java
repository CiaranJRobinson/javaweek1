public class Booleans {
    public static void main (String[] args) {
        int grade = 90;

        if (grade <= 50) {
            System.out.println("great job, you passed!");
        } else if (grade <= 100) {
            System.out.println("You got an A");
        } else{
            System.out.println("you didnt pass, try again");
        }
    }
}
